# Add missing keys to GroupView translation files
Write-Host "`nAdding missing keys to GroupView translation files..." -ForegroundColor Cyan
foreach ($lang in $translations["GroupView"].Keys) {
    $filePath = "FreeSpeakWeb\Resources\Pages\GroupView.$lang.resx"

    if (Test-Path $filePath) {
        # Read existing file
        $content = Get-Content -Path $filePath -Raw

        # Find the closing </root> tag and insert before it
        $insertPosition = $content.LastIndexOf('</root>')

        if ($insertPosition -gt 0) {
            $newEntries = ""
            foreach ($key in $translations["GroupView"][$lang].Keys) {
                $value = $translations["GroupView"][$lang][$key]
                $newEntries += @"

  <data name="$key" xml:space="preserve">
    <value>$value</value>
  </data>
"@
            }

            $newContent = $content.Substring(0, $insertPosition) + $newEntries + "`n" + $content.Substring($insertPosition)
            Set-Content -Path $filePath -Value $newContent -Encoding UTF8
            Write-Host "Updated: $filePath" -ForegroundColor Green
        }
    }
}

